/**
 * @typedef {{ name: string, color: string, strength: number }} ColorMatch
 */

/**
 * @param {string} colorText
 */
module.exports.findColor = function findColor(colorText) {

  /** @type {ColorMatch | undefined} */
  let exactMatch;

  /** @type {ColorMatch[] | undefined} */
  let strongMatches;

  /** @type {ColorMatch[] | undefined} */
  let weakMatches;

  colorText = decodeURIComponent(colorText);

  const lowercaseColorText = colorText.toLowerCase();
  const strongColorText = colorText.replace(/\([^)]\)/g, '').toLowerCase();
  const colorParts = strongColorText.split(/[^a-z]+/g);

  for (const key in colors) {
    const keyColor = colors[key];
    if (!/^#/.test(keyColor)) continue;

    const lowercaseKey = key.toLowerCase();
    if (lowercaseKey === lowercaseColorText) {
      exactMatch = { name: key, color: colors[key], strength: 0 };
      continue;
    }

    const strongMatchKey = lowercaseKey.replace(/\([^)]\)/g, '');
    if (strongMatchKey === strongColorText) {
      if (!strongMatches) strongMatches = [];
      strongMatches.push({ name: key, color: colors[key], strength: 1 });
      continue;
    }

    if (key.indexOf(strongColorText) >= 0) {
      if (!weakMatches) weakMatches = [];
      weakMatches.push({ name: key, color: colors[key], strength: 2 });
      continue;
    }
    else if (colorParts.length > 1) {
      for (const p of colorParts) {
        if (key.indexOf(p) >= 0) {
          if (!weakMatches) weakMatches = [];
          weakMatches.push({ name: key, color: colors[key], strength: 2 });
        }
      }
    }
  }

  return (exactMatch ? [exactMatch] : []).concat(strongMatches || [], weakMatches || []);
}


